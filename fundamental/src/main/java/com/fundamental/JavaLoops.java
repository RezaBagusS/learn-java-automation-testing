package com.fundamental;

public class JavaLoops {
    
    public static void main(String[] args) {
        
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        int l = 0;
        while (10 > l) {
            System.out.println(l);
        }
        
        int doWhile = 0;
        do {
            System.out.println(doWhile);
        } while (10 > doWhile);

    }

}
